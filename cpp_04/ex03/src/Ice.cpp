/* --------------------------- Default Constructor -------------------------- */

Ice::Ice( void ) :
	AMateria("ice")
{}

/* ------------------------ Assignation operator copy ----------------------- */

Ice&	Ice::operator=( const Ice& other )
{
	if (this != &other)
		_type = other._type;
	return (*this);
}

/* ---------------------------- Copy constructor ---------------------------- */

Ice::Ice( const Ice& other ) :
	AMateria( other )
{
	*this = other;
}

/* ------------------------------- Destructor ------------------------------- */

Ice::~Ice( void )
{}

